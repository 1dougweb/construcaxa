    <script>
        let map;
        let marker;
        let geocoder;
        let mapInitialized = false;
        let googleMapsLoaded = false;

        // Carregar Google Maps apenas quando necessário
        function loadGoogleMaps() {
            if (googleMapsLoaded) {
                initMap();
                return;
            }

            const script = document.createElement('script');
            script.src = 'https://maps.googleapis.com/maps/api/js?key=<?php echo e(config('services.google.maps_key')); ?>&libraries=places&callback=initMap';
            script.async = true;
            script.defer = true;
            script.onload = function() {
                googleMapsLoaded = true;
                // initMap será chamado pelo callback
            };
            document.head.appendChild(script);
        }

        function initMap() {
            // Verificar se o elemento existe
            const mapElement = document.getElementById('map');
            if (!mapElement) {
                console.warn('Map element not found, retrying...');
                setTimeout(initMap, 100);
                return;
            }

            if (mapInitialized) {
                return; // Já foi inicializado
            }

            geocoder = new google.maps.Geocoder();
            
            const defaultLocation = { lat: -23.5505, lng: -46.6333 }; // São Paulo
            
            // Verificar se já tem coordenadas
            const latitude = <?php echo \Illuminate\Support\Js::from($latitude)->toHtml() ?>;
            const longitude = <?php echo \Illuminate\Support\Js::from($longitude)->toHtml() ?>;
            
            if (latitude && longitude) {
                const location = { lat: parseFloat(latitude), lng: parseFloat(longitude) };
                map = new google.maps.Map(mapElement, {
                    zoom: 15,
                    center: location,
                });
                
                marker = new google.maps.Marker({
                    position: location,
                    map: map,
                    draggable: true,
                });
            } else {
                map = new google.maps.Map(mapElement, {
                    zoom: 15,
                    center: defaultLocation,
                });
            }

            // Clique no mapa para definir localização
            map.addListener('click', (e) => {
                const location = { lat: e.latLng.lat(), lng: e.latLng.lng() };
                
                if (marker) {
                    marker.setPosition(location);
                } else {
                    marker = new google.maps.Marker({
                        position: location,
                        map: map,
                        draggable: true,
                    });
                }

                // Atualizar coordenadas no Livewire
                window.Livewire.find('<?php echo e($_instance->getId()); ?>').set('latitude', location.lat);
                window.Livewire.find('<?php echo e($_instance->getId()); ?>').set('longitude', location.lng);

                // Geocodificar reverso para obter endereço
                geocoder.geocode({ location: location }, (results, status) => {
                    if (status === 'OK' && results[0]) {
                        window.Livewire.find('<?php echo e($_instance->getId()); ?>').set('address', results[0].formatted_address);
                    }
                });
            });

            // Autocomplete para endereço
            const addressInput = document.getElementById('address');
            if (addressInput) {
                const autocomplete = new google.maps.places.Autocomplete(addressInput);
                autocomplete.addListener('place_changed', () => {
                    const place = autocomplete.getPlace();
                    if (place.geometry) {
                        const location = {
                            lat: place.geometry.location.lat(),
                            lng: place.geometry.location.lng(),
                        };
                        
                        map.setCenter(location);
                        map.setZoom(15);
                        
                        if (marker) {
                            marker.setPosition(location);
                        } else {
                            marker = new google.maps.Marker({
                                position: location,
                                map: map,
                                draggable: true,
                            });
                        }

                        window.Livewire.find('<?php echo e($_instance->getId()); ?>').set('latitude', location.lat);
                        window.Livewire.find('<?php echo e($_instance->getId()); ?>').set('longitude', location.lng);
                    }
                });
            }

            mapInitialized = true;
        }

        // Escutar mudanças do Livewire para o Step 3
        document.addEventListener('livewire:init', () => {
            Livewire.on('stepChanged', (step) => {
                if (step === 3) {
                    // Aguardar o DOM atualizar
                    setTimeout(() => {
                        if (!mapInitialized) {
                            loadGoogleMaps();
                        }
                    }, 100);
                }
            });
        });

